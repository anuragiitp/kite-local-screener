/**
 * Generates src/screener/etfWatchlistData.js from Kite instruments dump.
 * Run: node scripts/generate-etf-watchlist.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { dedupeEquityEtfs } from './etfUnderlying.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const OUT = path.resolve(__dirname, '../src/screener/etfWatchlistData.js');
const DEFAULT_CSV = path.resolve(__dirname, 'data/kite_etfs_unique_nse_pref.csv');
const CSV_CANDIDATES = [
  process.env.ETF_CSV,
  DEFAULT_CSV,
].filter(Boolean);

function sourceLabel(filePath) {
  if (!filePath || filePath.includes('api.kite')) return filePath;
  const rel = path.relative(REPO_ROOT, filePath);
  if (!rel.startsWith('..')) return rel.split(path.sep).join('/');
  return filePath;
}

const PSEUDO_SYMBOL_RE = /(INAV|NAV)$/i;
const EXCLUDE_SYMBOLS = new Set(['FIRSTCRY']);

const CLASS_RULES = [
  ['Gold', /\bGOLD\b/i],
  ['Silver', /\bSILVER\b|\bSLVR\b/i],
  ['Liquid / 1D', /\bLIQUID\b|\b1D RATE\b|\bOVERNIGHT\b/i],
  ['G-Sec / Bond / Gilt', /\bG-SEC\b|\bGSEC\b|\bGILT\b|\bBOND\b|\bBHARAT BOND\b|\bSDL\b|\bSOVEREIGN\b/i],
];

/** One representative BEES per commodity bucket — duplicates track the same underlying. */
const PINNED_BEES = {
  Gold: 'GOLDBEES',
  Silver: 'SILVERBEES',
  'Liquid / 1D': 'LIQUIDBEES',
};

function isEtfName(name) {
  const n = (name || '').toUpperCase();
  return n.includes('ETF') || n.includes('BEES') || n.includes('EXCHANGE TRADED FUND');
}

function isPseudo(row) {
  const sym = (row.tradingsymbol || '').toUpperCase();
  if (PSEUDO_SYMBOL_RE.test(sym)) return true;
  if (sym.includes('INAV') || sym.endsWith('NAV')) return true;
  return false;
}

function classify(row) {
  const blob = `${row.tradingsymbol} ${row.name}`;
  for (const [label, rx] of CLASS_RULES) {
    if (rx.test(blob)) return label;
  }
  return 'Equity / Index / Thematic';
}

function shortName(name) {
  return String(name || '')
    .replace(/\s+ETF\b.*$/i, '')
    .replace(/\s+BEES\b.*$/i, '')
    .replace(/\s+EXCHANGE TRADED FUND.*$/i, '')
    .trim()
    .slice(0, 72) || name;
}

async function loadRows() {
  for (const csvPath of CSV_CANDIDATES) {
    if (csvPath && fs.existsSync(csvPath)) {
      const text = fs.readFileSync(csvPath, 'utf8');
      return { rows: parseCsv(text), source: sourceLabel(csvPath) };
    }
  }

  const res = await fetch('https://api.kite.trade/instruments');
  const text = await res.text();
  const all = parseCsv(text);
  const etfRows = all.filter((r) => r.instrument_type === 'EQ' && isEtfName(r.name) && !isPseudo(r));
  const bySymbol = new Map();
  for (const r of etfRows) {
    const sym = r.tradingsymbol.toUpperCase();
    if (EXCLUDE_SYMBOLS.has(sym)) continue;
    const prev = bySymbol.get(sym);
    if (!prev || (prev.exchange !== 'NSE' && r.exchange === 'NSE')) bySymbol.set(sym, r);
  }
  return { rows: [...bySymbol.values()], source: 'api.kite.trade/instruments' };
}

function parseCsv(text) {
  const lines = text.trim().split('\n');
  const headers = lines[0].split(',').map((h) => h.replace(/^"|"$/g, ''));
  return lines.slice(1).map((line) => {
    const cols = [];
    let cur = '';
    let inQuotes = false;
    for (let i = 0; i < line.length; i += 1) {
      const ch = line[i];
      if (ch === '"') {
        inQuotes = !inQuotes;
        continue;
      }
      if (ch === ',' && !inQuotes) {
        cols.push(cur);
        cur = '';
        continue;
      }
      cur += ch;
    }
    cols.push(cur);
    const row = {};
    headers.forEach((h, i) => { row[h] = cols[i] || ''; });
    return row;
  });
}

function buildSections(rows) {
  const buckets = new Map([
    ['Gold', []],
    ['Silver', []],
    ['Liquid / 1D', []],
    ['G-Sec / Bond / Gilt', []],
    ['Equity / Index / Thematic', []],
  ]);

  rows.forEach((row) => {
    const label = classify(row);
    buckets.get(label).push(row);
  });

  buckets.forEach((items, label) => {
    const pinned = PINNED_BEES[label];
    if (pinned) {
      const match = items.find((row) => row.tradingsymbol.toUpperCase() === pinned);
      buckets.set(label, match ? [match] : []);
      return;
    }
    if (label === 'Equity / Index / Thematic') {
      buckets.set(label, dedupeEquityEtfs(items));
      return;
    }
    items.sort((a, b) => a.tradingsymbol.localeCompare(b.tradingsymbol));
    buckets.set(label, items);
  });

  return buckets;
}

function emitJs(sections, meta) {
  const lines = [
    '// Auto-generated by scripts/generate-etf-watchlist.mjs — do not edit by hand.',
    `// Source: ${meta.source}`,
    `// Generated: ${meta.generated}`,
    `// Symbols: ${meta.count}`,
    '',
    'export const ETF_WATCHLIST_SECTIONS = [',
  ];

  for (const [label, items] of sections) {
    lines.push(`  { label: ${JSON.stringify(label)}, items: [`);
    items.forEach((row) => {
      lines.push('    {');
      lines.push(`      tradingsymbol: ${JSON.stringify(row.tradingsymbol)},`);
      lines.push(`      name: ${JSON.stringify(shortName(row.name))},`);
      lines.push(`      exchange: ${JSON.stringify(row.exchange || 'NSE')},`);
      lines.push(`      segment: ${JSON.stringify(row.segment || row.exchange || 'NSE')},`);
      lines.push(`      instrument_token: ${Number(row.instrument_token)},`);
      lines.push('    },');
    });
    lines.push('  ]},');
  }

  lines.push('];');
  lines.push('');
  lines.push('export const ETF_WATCHLIST_ITEMS = ETF_WATCHLIST_SECTIONS.flatMap(({ label, items }) => [');
  lines.push("  { type: 'separator', label },");
  lines.push('  ...items,');
  lines.push(']);');
  lines.push('');

  return lines.join('\n');
}

const { rows, source } = await loadRows();
const filtered = rows.filter((r) => !EXCLUDE_SYMBOLS.has((r.tradingsymbol || '').toUpperCase()));
const sections = buildSections(filtered);
const orderedSections = [
  ['Gold', sections.get('Gold')],
  ['Silver', sections.get('Silver')],
  ['Liquid / 1D', sections.get('Liquid / 1D')],
  ['Equity / Index / Thematic', sections.get('Equity / Index / Thematic')],
  ['G-Sec / Bond / Gilt', sections.get('G-Sec / Bond / Gilt')],
];

const js = emitJs(orderedSections, {
  source,
  generated: new Date().toISOString(),
  count: orderedSections.reduce((sum, [, items]) => sum + items.length, 0),
});

fs.writeFileSync(OUT, js, 'utf8');
const listed = orderedSections.reduce((sum, [, items]) => sum + items.length, 0);
console.log(`Wrote ${listed} watchlist ETFs -> ${OUT}`);
orderedSections.forEach(([label, items]) => console.log(`  ${label}: ${items.length}`));
